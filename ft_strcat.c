#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*s1alt;

	s1alt = s1;
	while (*s1alt != '\0')
		s1alt++;
	while (*s2 != '\0')
	{
		*s1alt = *s2;
		s1alt++;
		s2++;
	}
	*s1alt = '\0';
	return (s1);
}
